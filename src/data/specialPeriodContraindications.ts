export function findContraindications(
  drugName: string,
  specialPeriods: SpecialPeriodType[]
): Contraindication[] {
  if (specialPeriods.length === 0) return []

  const drugLower = drugName.toLowerCase().trim()
  return contraindicationKnowledgeBase.filter((contra) => {
    const drugMatch =
      contra.drugName.toLowerCase().includes(drugLower) ||
      contra.drugAliases.some((alias) => alias.toLowerCase().includes(drugLower))
    const periodMatch = specialPeriods.includes(contra.specialPeriod)
    return drugMatch && periodMatch
  })
}

export function findAllContraindicationsForPeriod(specialPeriod: SpecialPeriodType): Contraindication[] {
  return contraindicationKnowledgeBase.filter((contra) => contra.specialPeriod === specialPeriod)
}

